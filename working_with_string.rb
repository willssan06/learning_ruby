puts phrase.length() # conta os caracteres
puts phrase.include? "Academy" # retorna verdadeiro ou falso
puts phrase[0] # retonar o valor no indice especificado
puts phrase[0,3] # retonar a cadeia de caracteres através do indices especificados
puts phrase.index("G") # retonar o indice do caracter especificado
